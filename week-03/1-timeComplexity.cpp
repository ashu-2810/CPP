// time & space complexity
// time complexity:- amount of time taken by algorithm to run as function of length of input
// space complexity:- amount of space taken by algorithm to run as function of length of input

// Unit to represent complexity
// big O:- upper bound
// theta ∅:- average bound
// omega ʊ: lower bound

// big O complexity
// constant time: O(1)
// linear time: O(n)
// logarithmic time: O(log n)
// quadratic time: O(n²)
// cubic time: O(n³)

// O(1) < O(log n) < O(√n) < O(n) < O(n log n) < O(n²) < O(n³) < O(2ⁿ) < O(n!) < O(nⁿ)